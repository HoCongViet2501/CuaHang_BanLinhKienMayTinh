    private void btnChonanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonanhActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        chooser.setDialogTitle("Chọn ảnh");

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Image","jpg", "png");
        chooser.addChoosableFileFilter(filter);

        int result = chooser.showSaveDialog(null);
        File file = chooser.getSelectedFile();
        String filename = file.getName();
        if(filename.endsWith(".jpg")||filename.endsWith(".JPG")||filename.endsWith(".png")||filename.endsWith(".PNG")){
            if(result == JFileChooser.APPROVE_OPTION){
                path = file.getAbsolutePath();
                ImageIcon imgIcon = new ImageIcon(path);
                Image img = imgIcon.getImage();

                Image newImg = img.getScaledInstance(labelHinhAnh.getWidth(), labelHinhAnh.getHeight(), Image.SCALE_SMOOTH);

                ImageIcon icon = new ImageIcon(newImg);
                labelHinhAnh.setIcon(icon);

            }
        }else
        JOptionPane.showMessageDialog(this,"Chọn sai tệp, vui lòng chọn tệp hình ảnh");

    }//GEN-LAST:event_btnChonanhActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) throws ClassNotFoundException {//GEN-FIRST:event_btnTimActionPerformed
    	String tukhoa = txtTenSPTimKiem.getText();
    	DefaultTableModel model = new DefaultTableModel(new Object[]{"Mã sản phẩm", "Tên sản phẩm", "Số lượng tồn", "Đơn giá","Loại sản phẩm","Nhà cung cấp"}, 0);
    	
    	List<SanPham> dsDungcu= new DAO_Sanpham().getSanPham(tukhoa);
    	for(SanPham sanpham :dsDungcu) {
    		Object[] tableModel = {sanpham.getMaSP(), sanpham.getTenSP(), sanpham.getSlton(), 
    				sanpham.getDonGia(), sanpham.getMaNCC().getMaNCC(),sanpham.getMaLoaiSP().getMaLoaiSP()};
    		model.addRow(tableModel);
    	}
    	tableSP.setModel(model);
    }//GEN-LAST:event_btnTimActionPerformed

