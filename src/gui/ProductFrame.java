    private void showProductTable() throws ClassNotFoundException {
        DefaultTableModel model = new DefaultTableModel(new Object[]{ "Mã sản phẩm", "Tên sản phẩm", "Số lượng tồn", "Đơn giá","Nhà cung cấp","Loại sản phẩm" }, 0);
    	DAO_Sanpham dao_sanpham = new DAO_Sanpham();
    	List<SanPham> list = dao_sanpham.getSanPham("");
    	if(list.size()==0) {
    		JOptionPane.showMessageDialog(this,"Không tìm thấy sản phẩm, Vui lòng thử lại");
    	}else {
        	for(SanPham sanpham : list) {
        		
        		Object[] tableModel = {sanpham.getMaSP(), sanpham.getTenSP(), sanpham.getSlton(), 
        				sanpham.getDonGia(), sanpham.getMaNCC().getTenNCC(),sanpham.getMaLoaiSP().getTenLoaiSP()};
        		model.addRow(tableModel);
        	}
        	tableSP.setModel(model);
    	}

    }
