    private void tableSPMouseClicked(java.awt.event.MouseEvent evt) throws ClassNotFoundException {//GEN-FIRST:event_tableSPMouseClicked
        // TODO add your handling code here:
    	int click = tableSP.getSelectedRow();
		TableModel tableModel = tableSP.getModel();
		cbLoaiSP.setSelectedItem(tableModel.getValueAt(click, 4).toString());
		txtMaSP.setText(tableModel.getValueAt(click, 0).toString());
		txtTenSP.setText(tableModel.getValueAt(click, 1).toString());
		txtDonGia.setText(tableModel.getValueAt(click, 3).toString());
		txtSoLuongTon.setText(tableModel.getValueAt(click, 2).toString());
		if(Integer.parseInt(tableModel.getValueAt(click, 2).toString()) > 0){
			txtTrangThai.setText("Còn hàng");
		}else
			txtTrangThai.setText("Hết hàng");
		cbMaNCC.setSelectedItem(tableModel.getValueAt(click,5).toString());
		btnSua.setEnabled(true);
		btnXoa.setEnabled(true);
		DAO_Sanpham dao_sanpham = new DAO_Sanpham();
		try {
			
			path = dao_sanpham.getImg(tableModel.getValueAt(click, 0).toString());
			ImageIcon icon = new ImageIcon(path);
			Image img = icon.getImage();
			Image newImg = img.getScaledInstance(labelHinhAnh.getWidth(), labelHinhAnh.getHeight(), Image.SCALE_SMOOTH);
			ImageIcon imgIcon = new ImageIcon(newImg);
			labelHinhAnh.setIcon(imgIcon);
		
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    }//GEN-LAST:event_tableSPMouseClicked

